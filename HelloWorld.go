package main

import "fmt"

func main() {
	//mencetak secara biasa
	fmt.Println("Abdul Kadir Hasani")
	//mencetak double parameter strin dan decimal
	fmt.Println("1+1 =", 1+1)
	//kondisi bernilai boolean
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	fmt.Println()
	fmt.Print("My Name is")
	fmt.Print(" Abdul Kadir Hasani")
}
